#include <sys/types.h> // ssize_t
#include <unistd.h> //STDIN_FILENO STDOUT_FILENO read write


int main()
{
	char* c;
	ssize_t curr = read(STDIN_FILENO, &c, 1);
	while(curr > 0)
	{
		write(STDOUT_FILENO, &c, 1);
		curr = read(STDIN_FILENO, &c, 1);
	}

	return 0;
}


